const sum = (num1,num2) => num1 + num2
const diff = (num1,num2) => num1 - num2
const prod = (num1,num2) => num1 * num2
const quot = (num1,num2) => num1 / num2
const sqrt = (num1) => Math.sqrt(num1)
const max = (num1,num2) => Math.max(num1,num2)

module.exports = {
    sum,
    diff,
    prod,
    quot,
    sqrt,
    max
}